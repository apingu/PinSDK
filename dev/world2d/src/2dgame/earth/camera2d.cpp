    GameVertex Camera2d::GetPosition(void)
    {
        GameVertex tp = GSO::World::IEntity2d::GetRefOrigin();
        tp.x = -tp.x;
        tp.y = -tp.y;
        return tp;
        //return m_Position;
    }

    //==========================================================================================
    // NAME:
    // DESC: 
    //==========================================================================================
    void Camera2d::SetPosition(GameVertex pos)
    {
        GameVertex tp = pos;
        tp.x = -tp.x;
        tp.y = -tp.y;
        GSO::World::IEntity2d::SetRefOrigin(tp);
    }

